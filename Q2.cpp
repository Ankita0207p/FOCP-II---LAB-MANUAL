// Average Marks of three students
#include<iostream>
int main()
{
    int m1,m2,m3;
    std::cout<<"Enter Marks of first student: ";
    std::cin>>m1;
    std::cout<<"Enter Marks of second student: ";
    std::cin>>m2;
    std::cout<<"Enter Marks of third student: ";
    std::cin>>m3;
    std::cout<<"Average Marks of three students are: "<<(m1+m2+m3)/3;
    return 0;
}